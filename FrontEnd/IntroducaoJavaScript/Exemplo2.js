//  Tipos de Operadores

// *Operadores Aritméticos* (+ - * / %)
var a = 10;
var b = 3;
var c;

// Adição
c = a + b;
console.log("Soma: "+c)

// Subtração
c = a - b
console.log("Subtração: "+c)

// Multiplicação
c = a * b;
console.log("Multiplicação: "+c)

// Divisão
c = a / b;
console.log("Divisão: "+c)

// Resto da Divisão
c = a % b;
console.log("Resto: "+c)

//  *Operadores Relacionais* ( > < >= <= == === != !==)
var a = 10;
var b = 20;
var c = "20";

console.log("Relacionais: "+(a>b)); //False
// O que importa
console.log("Igualdidade Simples: " +(b==c)) //True
console.log("Igualdade Estrita: " +(b===c)) //False

